# -*- coding: utf-8 -*-

class BookedRoom:
    def __init__(self, guestID, roomNumber, guestCount, checkInMonth, checkInDay, checkOutMonth, checkOutDay):
        self.guestID = guestID
        self.roomNumber = roomNumber
        self.guestCount = guestCount
        self.checkInMonth = checkInMonth
        self.checkInDay = checkInDay
        self.checkOutMonth = checkOutMonth
        self.checkOutDay = checkOutDay